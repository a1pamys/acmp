#include <iostream>

using namespace std;

int main() {
    
    int n,k, ans = 1, i, temp;
    scanf("%d %d", &n, &k);
    
    for(i=n; i>n-k; i--) {
        ans *= i;
    }
    
    temp = ans;
    
    /*
     
     1 ->                                           1^2 / 1! = 64
          _ * n...(n-k+1) / k!
          8 * 8 / 1
     2 ->                                     2^2 * 1^2 / 2! =
          _ _ * n...(n-k+1)
          8 7 * 8 * 7 / 2!
     3 ->                               3^2 * 2^2 * 1^2 / 3! =
     4 ->                         4^2 * 3^2 * 2^2 * 1^2 / 4! =
     5 ->                   5^2 * 4^2 * 3^2 * 2^2 * 1^2 / 5! =
          _ _ _ _ _ n...(n-k+1) / k!
          8*7*6*5*4 * 8*7*6*5*4 / 5!
     6 ->             6^2 * 5^2 * 4^2 * 3^2 * 2^2 * 1^2 / 6! =
     7 ->       7^2 * 6^2 * 5^2 * 4^2 * 3^2 * 2^2 * 1^2 / 7! = 322560
          _ _ _ _ _ _ _ n...(n-k+1) / k!
          8*7*6*5*4*3*2 * 8*7*6*5*4*3*2 / 7!
     8 -> 8^2 * 7^2 * 6^2 * 5^2 * 4^2 * 3^2 * 2^2 * 1^2 / 8! = 40320
          _ _ _ _ _ _ _ _ n...(n-k+1) / k!
          8*7*6*5*4*3*2*1 * 8*7*6*5*4*3*2*1 / 8!

     n!/(n-k)! * n!/(n-k)! / k!
     n*(n-1)*(n-2)*...*2*1 / (n-k)*(n-k-1)*...*2*1 * n*(n-1)*(n-2)*...*2*1 / (n-k)*(n-k-1)*...*2*1  / k*(k-1)*...*2*1
     
     */
    
    for(int i=2; i<=k; i++) {
        temp /= i;
    }
    
    ans *= temp;
    
    printf("%d", ans);
    
    return 0;
}
